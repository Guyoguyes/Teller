CREATE TABLE CATEGORY(
    CAT_ID SERIAL  PRIMARY KEY,
    CATEGORY_NAME VARCHAR NOT NULL
);

CREATE TABLE NEWS(
    NEWS_ID SERIAL PRIMARY KEY ,
    CAT_ID SERIAL NOT NULL,
    AUTHOR_ID SERIAL NOT NULL,
    CONTENT VARCHAR NOT NULL,
    CREATED_AT TIMESTAMP NOT NULL
);

CREATE TABLE AUTHOR(
    AUTHOR_ID SERIAL PRIMARY KEY,
    FIRSTNAME VARCHAR NOT NULL,
    LASTNAME VARCHAR NOT NULL,
    TITLE VARCHAR NOT NULL
);

CREATE TABLE COMMENT(
    COMMENT_ID SERIAL PRIMARY KEY,
    NEWS_ID SERIAL NOT NULL,
    CONTENT VARCHAR NOT NULL,
    COMMENT_DATE TIMESTAMP NOT NULL
);

ALTER TABLE NEWS ADD FOREIGN KEY (CAT_ID) REFERENCES CATEGORY(CAT_ID);
ALTER TABLE NEWS ADD FOREIGN KEY (AUTHOR_ID) REFERENCES AUTHOR(AUTHOR_ID);

ALTER TABLE COMMENT ADD FOREIGN KEY (NEWS_ID) REFERENCES NEWS(NEWS_ID);
